// пример за рекурсия. Функция за намиране на факториел от число зададено,
// като аргумент. Факториелът е произведението от всички числа от 1 до
// числото включително. Примерно, факториел от числото 5 е 1*2*3*4*5 = 120.
function factorial(n) {
    if (n == 0) {
        return 1;
    }
    return factorial(n-1) * n;
}

console.log(factorial(4));